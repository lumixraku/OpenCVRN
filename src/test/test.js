let offsetXPreviewDefault = 180
let offsetYPreviewDefault = 250

const addOffsetForFaceData = (previewOffset, target) => {
    const checkedType = (target) => {
        return Object.prototype.toString.call(target).slice(8, -1)
    }
    //判断拷贝的数据类型
    //初始化变量result 成为最终克隆的数据
    let result
    let targetType = checkedType(target)
    if (targetType === 'Object') {
        result = {}
    } else if (targetType === 'Array') {
        result = []
    } else {
        return target
    }



    //遍历目标数据  target is {...}
    for (let [key, value] of Object.entries(target)) {
        //获取遍历数据结构的每一项值。
        // let value = target[key]
        //判断目标结构里的每一值是否存在对象/数组
        if (checkedType(value) === 'Object' ||
            checkedType(value) === 'Array') { //对象/数组里嵌套了对象/数组
            //继续遍历获取到value值
            result[key] = addOffsetForFaceData(previewOffset, value)
        } else { //获取到value值是基本的数据类型或者是函数。
            // 一些诡异的点 检测到的(0, 0) 这样的情况
            if (value === 0) {
                continue
            }

            if (key == "x") {
                result[key] = offsetXPreviewDefault + previewOffset.x + (value)

            } else if (key == "y") {
                result[key] = offsetYPreviewDefault + previewOffset.y + (value)

            } else {
                result[key] = value;
            }
        }
    }
    return result
}

